# main.py
def start_game():
    print("Добро пожаловать в текстовый квест!")
    print("Вы находитесь в тёмном лесу. Перед вами две тропинки.")
    print("1. Пойти налево")
    print("2. Пойти направо")
    choice = input("Ваш выбор (1 или 2): ")
    if choice == "1":
        print("Вы пошли налево и нашли волшебный меч! Вы победили дракона. Поздравляю!")
    elif choice == "2":
        print("Вы пошли направо и попали в болото. Квест окончен.")
    else:
        print("Неверный ввод. Попробуйте ещё раз.")

if __name__ == "__main__":
    start_game() 
